package com.aaa_battery.aaa_batteryproject.reviews.dto;

import java.util.Date;

public class ReviewResponseDTO {
    
    private Long reviewId;
    private Integer borrowId;
    private Integer itemDescriptionId;
    private String reviewerName;
    private String itemName;
    private Integer rating;
    private String comment;
    private Date createdAt;
    private Date updatedAt;
    
    // Getters and setters
    public Long getReviewId() {
        return reviewId;
    }
    
    public void setReviewId(Long reviewId) {
        this.reviewId = reviewId;
    }

    public Integer getBorrowId() {
        return borrowId;
    }

    public void setBorrowId(Integer borrowId) {
        this.borrowId = borrowId;
    }

    public Integer getItemDescriptionId() {
        return itemDescriptionId;
    }

    public void setItemDescriptionId(Integer itemDescriptionId) {
        this.itemDescriptionId = itemDescriptionId;
    }
    
    public String getReviewerName() {
        return reviewerName;
    }
    
    public void setReviewerName(String reviewerName) {
        this.reviewerName = reviewerName;
    }
    
    public String getItemName() {
        return itemName;
    }
    
    public void setItemName(String itemName) {
        this.itemName = itemName;
    }
    
    public Integer getRating() {
        return rating;
    }
    
    public void setRating(Integer rating) {
        this.rating = rating;
    }
    
    public String getComment() {
        return comment;
    }
    
    public void setComment(String comment) {
        this.comment = comment;
    }
    
    public Date getCreatedAt() {
        return createdAt;
    }
    
    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }
    
    public Date getUpdatedAt() {
        return updatedAt;
    }
    
    public void setUpdatedAt(Date updatedAt) {
        this.updatedAt = updatedAt;
    }
}